package Arrays;

public class Multidimensional1 
{
	// 	10	20	70
//		40	50	80
//		30	60	90
	
public static void main(String[] args) 
{
 int A1[][]={{10,20,70},{40,50,80},{30,60,90}};
	
 for (int a=0; a<=2; a++) 
 {
	 for(int b=0; b<=2; b++) 
	 {
		 System.out.print(A1[a][b]+" ");
	 }
	 System.out.println();
 }
	
	
}

	
	
	
	
	
	
	
	
}
