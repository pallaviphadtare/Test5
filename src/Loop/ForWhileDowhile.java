package Loop;

public class ForWhileDowhile
{
public static void main(String[] args) 
{
for(int A=5; A>=1; A=A-2) 
{
System.out.println(A);
}	
	int A=5;
	while(A>=1) 
	{
		System.out.println(A);
		A=A-2;
	}
	int i=5;
	do 
	{
	System.out.println(i);
	i=i-2;
	}
	while(i>=1);
	
}
}
