package Loop;

public class For_While_Dowhile
{
public static void main(String[] args)
{
for(int a=50; a>=10; a=a-4) 
{
System.out.println(a);	
}
	int a=10;
	while(a<=30) 
	{
		System.out.println(a);
		a=a+3;
	}
	int b=10;
	do 
	{
		System.out.println(b);
		b=b-2;
	}
	while(b>=1);
}
}
